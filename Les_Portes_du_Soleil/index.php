        <h4>Die Skiregion</h4>
        <div>
          <img src="https://www.snowtrex.de/magazin/files/2017/08/CHAT_Skigebiet_presse.chatel.com%C2%A9JF-Vuarand-2.jpg" width="250px" height="166px" id="change"/>
          <p>
            Die Skiregion Les Portes du Soleil besteht aus einem Verbund von zwölf einzelnen Skigebieten in der Grenzregion zwischen der Schweiz und Frankreich und ist mit 600 Pistenkilometern verteilt auf rund 285 Pisten und 196 Liftanlagen das grösste Skigebiet der Schweiz. Das Skigebiet erstreckt sich von 1000 m bis auf fast 2500 m ü. M..<br /><br />
            Es gibt Pisten in verschiedenen Schwierigkeitsgraden: 31 grüne Pisten für Anfänger, 122 blaue Pisten für Fortgeschrittene, 100 rote Pisten für Könner und 32 schwarze Pisten als Herausforderung für exzellente Skifahrer.<br /><br />
            Es gibt ausserdem 21 verschiedene Snowparks in denen die TrickskiLiebhaber auf ihren Kosten kommen.<br /><br />
            Das Schweiz-Französische Skigebiet beinhaltet 12 Orte, von denen 8 auf französischer und 4 auf Schweizer Seite liegen. Trotzdem wird nur ein internationaler Skipass verlangt, der für das gesamte Skigebiet gültig ist.<br /><br />
            Das Portes du Soleil ist aber nicht nur Skigebiet, sondern hat auch abseits der Pisten viel zu bieten. So z.B: 280 km gespurte Loipen für <a href="https://de.portesdusoleil.com/aktivitaeten/winter/nordischer-ski.html">Langläufer</a>, mehrere Kunsteisbahnen und viele Möglichkeiten zum Schlitteln. In der Region Portes du Soleil kann auf  über 60 ausgesteckten Touren <a href="https://www.bergfex.ch/sommer/portes-du-soleil/touren/winterwandern/">Winter-gewandert</a> werden, es besteht die Möglichkeit, einen Hundeschlitten zu mieten und sogar Eisklettern und Tauchen ist hier möglich.
